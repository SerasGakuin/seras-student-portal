/**
 * 名前を正規化する
 *
 * 以下の処理を行う:
 * - 前後の空白をトリム
 * - 内部の空白文字（スペース、タブ、改行、全角スペース）を削除
 * - ゼロ幅文字（U+200B-U+200D, U+FEFF）を削除
 *
 * @param name 正規化する名前
 * @returns 正規化された名前
 */
export function normalizeName(name: string): string {
    // 空白文字（半角/全角スペース、タブ、改行など）とゼロ幅文字を削除
    // \s: 通常の空白文字
    // \u3000: 全角スペース
    // \u200B-\u200D: ゼロ幅スペース、ゼロ幅非接合子、ゼロ幅接合子
    // \uFEFF: BOM / ゼロ幅非改行スペース
    return name.replace(/[\s\u3000\u200B-\u200D\uFEFF]/g, '');
}
